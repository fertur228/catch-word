/**
 * Переключатель «Диагностика скана» (Настройки → Дополнительно).
 *
 * Зачем: замеры этапов скана печатаются в дев-логе, но в TestFlight/сторе
 * консоли нет — а именно там и нужны честные цифры с реального телефона и
 * реальной сети. Флаг включает показ разбивки прямо на экране Результата.
 *
 * По умолчанию ВЫКЛЮЧЕН: сборка с включённой диагностикой не должна случайно
 * уехать в стор с отладочным блоком на экране. Хранится локально (key_value),
 * в аккаунт не синхронизируется — это свойство устройства, а не пользователя.
 */
import * as db from '@/lib/db';

const KEY = 'scan_diagnostics';

let enabled = false;

/** Значение читается один раз при старте — к первому скану уже загружено. */
void db
  .getPref(KEY)
  .then((v) => {
    enabled = v === '1';
  })
  .catch(() => {
    // нет доступа к хранилищу — диагностика просто выключена
  });

/** Включена ли диагностика скана (синхронно — зовём из потока скана). */
export function isScanDiagOn(): boolean {
  return enabled;
}

/** Перечитать флаг из хранилища (экран Настроек — показать актуальное состояние). */
export async function loadScanDiag(): Promise<boolean> {
  try {
    enabled = (await db.getPref(KEY)) === '1';
  } catch {
    // оставляем как есть
  }
  return enabled;
}

/** Переключить и запомнить. */
export async function setScanDiag(on: boolean): Promise<void> {
  enabled = on;
  try {
    await db.setPref(KEY, on ? '1' : '0');
  } catch {
    // не сохранилось — но в этой сессии флаг уже применён
  }
}
