/**
 * Русские склонения по числу. Маленькие чистые помощники для подписей в UI
 * («1 слово / 2 слова / 5 слов»), чтобы не дублировать логику по экранам.
 */

/** Склонение слова «слово» по числу: 1 слово / 2 слова / 5 слов. */
export function pluralWords(n: number): string {
  const m10 = n % 10;
  const m100 = n % 100;
  if (m10 === 1 && m100 !== 11) return 'слово';
  if (m10 >= 2 && m10 <= 4 && (m100 < 10 || m100 >= 20)) return 'слова';
  return 'слов';
}
