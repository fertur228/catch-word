/**
 * Общие типы приложения.
 *
 * `WordCard` повторяет модель `word_card` из спеки (§7.2). В MVP «стикер» —
 * это эмодзи (`emoji`); в реальной версии вместо него будет вырезанное фото
 * предмета (`imageUri`).
 */

/** Тарифы из спеки §8. */
export type Tier = 'free' | 'basic' | 'premium';

/** Оценка пользователя при повторе карточки (SRS, спека §5.6). */
export type SrsRating = 'again' | 'good' | 'easy';

/** Сохранённая карточка слова (одна запись в локальной БД). */
export interface WordCard {
  id: string;
  /** Эмодзи-заглушка вместо стикера (MVP). */
  emoji: string;
  /** Реальное фото/стикер — появится позже (спека §5.3, `[later]`). */
  imageUri?: string | null;
  /** Слово на изучаемом языке (в спеке — `object_label`). */
  word: string;
  /** Перевод на родной язык. */
  translation: string;
  /** Транскрипция, в спеке — `pronunciation_ipa`. */
  ipa: string;
  /** Примеры употребления (в БД хранятся как JSON-строка). */
  examples: string[];
  /** Категория предмета (еда, мебель, …). */
  category?: string | null;
  /** Язык изучения в формате BCP-47, напр. 'en-US' — нужен для озвучки. */
  learningLang: string;
  /** Родной язык, напр. 'ru-RU'. */
  nativeLang: string;
  /** Время сохранения (Unix ms). */
  createdAt: number;
  /**
   * Владелец карточки — id аккаунта, создавшего её (или null для гостя).
   * Локальная БД общая на устройство, поэтому показываем/синхронизируем только
   * карточки текущего аккаунта; чужие не видны и не утекают в его облако.
   */
  ownerId?: string | null;

  // --- Поля интервального повторения (SRS, спека §5.6). Опциональны: ---
  // у «распознанных» слов и старых карточек их может не быть; контекст
  // коллекции инициализирует их при добавлении (см. collection-context).
  /** Когда карточку нужно повторить (Unix ms). dueAt<=now → пора. */
  dueAt?: number;
  /** Текущий интервал до следующего повтора, в минутах. */
  interval?: number;
  /** Лёгкость (SM-2): множитель роста интервала, ~1.3..3.0. */
  ease?: number;
  /** Сколько раз карточку успешно повторили подряд. */
  reps?: number;
  /** Уровень освоения 0..5 (для прогресса/звёзд в UI). */
  mastery?: number;
  /** Личная заметка / мнемоника (от AI или пользователя, опционально). */
  notes?: string;
  /** Правдоподобные неправильные переводы (AI) — для умных вариантов в тесте. */
  distractors?: string[];
}

/** «Распознаваемый» предмет — то, что бэкенд вернул бы по фото. */
export type RecognizableWord = Omit<WordCard, 'id' | 'createdAt'>;

/** Язык в списке выбора (онбординг/настройки). */
export interface AppLanguage {
  /** BCP-47 код, напр. 'en-US' — им же пользуется озвучка. */
  code: string;
  /** Человекочитаемое название, напр. 'English'. */
  label: string;
  /** Эмодзи-флаг, напр. '🇺🇸'. */
  flag: string;
}

/** Пользовательские настройки (хранятся в key_value таблице БД). */
export interface UserPrefs {
  /** Язык изучения (BCP-47). */
  learningLang: string;
  /** Родной язык (BCP-47). */
  nativeLang: string;
  /** Прошёл ли пользователь онбординг. Пока false — показываем онбординг. */
  onboarded: boolean;
}

/** Простая сводка по коллекции для дашборда/вкладок. */
export interface CollectionStats {
  /** Всего карточек. */
  total: number;
  /** Сколько освоено (mastery>=4). */
  mastered: number;
  /** Сколько карточек пора повторить сейчас (dueAt<=now). */
  dueCount: number;
  /** Текущая серия дней с активностью (мок-простая). */
  streak: number;
}

/** Описание тарифа для экрана Пейволла. */
export interface Plan {
  tier: Tier;
  name: string;
  /** Короткая строка с ценой, как в спеке §8. */
  price: string;
  /** Подпись под ценой (период / условия). */
  priceNote?: string;
  features: string[];
  /** Подсветить как «лучший выбор» (годовой Premium). */
  highlighted?: boolean;
  /** Бейдж сверху карточки, напр. «Best Value». */
  badge?: string;
  ctaLabel: string;
}
